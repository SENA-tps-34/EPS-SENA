
package Modelo;


public class Medico {
    
    int MedIdentificacion;
    String MedNombre;
    String MedApellidos;
    String MedPassword;

    public int getMedIdentificacion() {
        return MedIdentificacion;
    }

    public void setMedIdentificacion(int MedIdentificacion) {
        this.MedIdentificacion = MedIdentificacion;
    }

    public String getMedNombre() {
        return MedNombre;
    }

    public void setMedNombre(String MedNombre) {
        this.MedNombre = MedNombre;
    }

    public String getMedApellidos() {
        return MedApellidos;
    }

    public void setMedApellidos(String MedApellidos) {
        this.MedApellidos = MedApellidos;
    }

    public String getMedPassword() {
        return MedPassword;
    }

    public void setMedPassword(String MedPassword) {
        this.MedPassword = MedPassword;
    }
    
}
